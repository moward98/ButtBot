# List of pins used

# Motor control pins
R_MTR_DIR = 11     # GPIO pin to control right motor direction
L_MTR_DIR = 13     # GPIO pin to control left motor direction

R_MTR = 32         # PWM pin to control right motor
L_MTR = 33         # PWM pin to control left motor

# Ultrasonic sensor i2c bus
US_I2C_SDA = 3
US_I2C_SCL = 5

# Servo driver board i2c bus
SD_I2C_SDA = 27
SD_I2C_SCL = 28